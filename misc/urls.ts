type UrlSearchResult = {
  url: string;
  similarity: number;
};

function levenshtein(a: string, b: string): number {
  let tmp;
  let i, j;
  const alen = a.length;
  const blen = b.length;
  const row: number[] = Array(blen + 1).fill(0);
  const d: number[][] = Array(alen + 1)
    .fill(0)
    .map(() => Array(blen + 1).fill(0));

  if (alen === 0) return blen;
  if (blen === 0) return alen;

  for (i = 0; i <= blen; i++) row[i] = i;

  for (i = 1; i <= alen; i++) {
    d[i][0] = i;
    for (j = 1; j <= blen; j++) {
      tmp = a[i - 1] === b[j - 1] ? 0 : 1;
      d[i][j] = Math.min(
        d[i - 1][j] + 1,
        d[i][j - 1] + 1,
        d[i - 1][j - 1] + tmp,
      );
    }
  }
  return d[alen][blen];
}
export function searchUrl(searchName: string): UrlSearchResult[] {
  const normalizedSearchName = searchName
    .toLowerCase()
    .replace(/^(mr|ms|dr)\.?\s+/, "")
    .replace(/\./g, " ")
    .replace(/\s+/g, " ")
    .trim();

  const searchTokens = normalizedSearchName.split(" ");
  const hasDrInSearch = searchTokens.includes("dr");

  const results = urls
    .map((url) => {
      const decodedUrl = decodeURIComponent(url);
      const urlName = decodedUrl.split("/").pop() || "";
      const normalizedUrlName = urlName
        .toLowerCase()
        .replace(/^(mr|ms|dr)-?/, "")
        .replace(/-/g, " ")
        .replace(/\./g, " ")
        .replace(/\s+/g, " ")
        .trim();

      const urlTokens = normalizedUrlName.split(" ");

      const distance = levenshtein(normalizedSearchName, normalizedUrlName);
      const maxLen = Math.max(
        normalizedSearchName.length,
        normalizedUrlName.length,
      );
      const distanceScore = 1 - distance / maxLen;

      let tokenMatchScore = 0;
      const exactMatches = searchTokens.filter((token) =>
        urlTokens.includes(token),
      ).length;
      const partialMatches = searchTokens.filter((token) =>
        urlTokens.some((urlToken) => urlToken.includes(token)),
      ).length;

      tokenMatchScore += exactMatches * 2;
      tokenMatchScore += partialMatches * 0.5;

      let drBonus = 0;
      if (hasDrInSearch && urlTokens.includes("dr")) {
        drBonus = 1;
      }

      const finalScore = Math.max(distanceScore + tokenMatchScore + drBonus, 0);

      if (
        !normalizedSearchName.includes(normalizedUrlName[0]) ||
        !normalizedSearchName.includes(
          normalizedUrlName[normalizedUrlName.length - 1],
        )
      )
        return {
          url,
          similarity: 0,
        };

      return {
        url,
        similarity: finalScore,
      };
    })
    .filter((result) => result.similarity > 1)
    .sort((a, b) => b.similarity - a.similarity);

  return results.splice(0, 3);
}
